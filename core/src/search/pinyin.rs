pub(super) fn initials_for(value: &str) -> String {
    value
        .split(|character: char| !character.is_alphanumeric())
        .flat_map(token_initials)
        .collect()
}

fn token_initials(token: &str) -> Vec<char> {
    let mut initials = Vec::new();
    for character in token.chars() {
        if let Some(initial) = chinese_initial(character) {
            initials.push(initial);
        }
    }

    if initials.is_empty() {
        token
            .chars()
            .next()
            .map(|character| character.to_ascii_lowercase())
            .into_iter()
            .collect()
    } else {
        initials
    }
}

fn chinese_initial(character: char) -> Option<char> {
    COMMON_CHINESE_INITIALS
        .iter()
        .find_map(|(candidate, initial)| (*candidate == character).then_some(*initial))
}

